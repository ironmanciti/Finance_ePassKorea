# ## 1. 정적 보고서 vs 인터랙티브 대시보드
#
# ### 기존 보고 방식의 한계
#
# | 항목 | 정적 보고서 (Excel/PPT) | 인터랙티브 대시보드 |
# |------|------------------------|-------------------|
# | **데이터 갱신** | 수동 (복사/붙여넣기) | 자동 (실시간) |
# | **분석 범위** | 고정된 뷰 | 사용자가 필터링/드릴다운 |
# | **배포** | 파일 첨부 이메일 | URL 공유 |
# | **협업** | 버전 관리 어려움 | 동일 데이터 공유 |
# | **유지보수** | 매번 수정 필요 | 코드 수정 후 자동 반영 |
#
# ### 인터랙티브 대시보드의 장점
#
# 1. **실시간 데이터 반영**
#    - 주가, 환율, 경제지표 등 실시간 업데이트
#    - 수동 갱신 작업 불필요
#
# 2. **사용자 맞춤 분석**
#    - 종목 선택, 기간 조정, 지표 변경
#    - 동일 대시보드로 다양한 분석 가능
#
# 3. **의사결정 속도 향상**
#    - 필요한 정보를 즉시 확인
#    - 데이터 기반 빠른 판단

