# ## 3. 금융 대시보드 사례
#
# ### 사례 1: 포트폴리오 모니터링
# ```
# ┌─────────────────────────────────────────────────────┐
# │ [종목 선택] [기간 선택]                              │
# ├──────────────────────┬──────────────────────────────┤
# │ 포트폴리오 구성비     │ 수익률 추이 차트             │
# │ (파이 차트)          │ (라인 차트)                  │
# ├──────────────────────┴──────────────────────────────┤
# │ 종목별 수익률 테이블                                │
# └─────────────────────────────────────────────────────┘
# ```
#
# ### 사례 2: 경제 지표 모니터링
# ```
# ┌─────────────────────────────────────────────────────┐
# │ [지표 선택: 금리 / CPI / 실업률]                     │
# ├─────────────────────────────────────────────────────┤
# │ 선택한 지표 시계열 차트                             │
# │ (인터랙티브: 줌, 호버 정보)                         │
# ├─────────────────────────────────────────────────────┤
# │ 최근 발표 데이터 테이블                             │
# └─────────────────────────────────────────────────────┘
# ```
#
# ### 사례 3: 일일 시황 리포트
# ```
# ┌─────────────────────────────────────────────────────┐
# │ [날짜 선택]                                         │
# ├──────────────────────┬──────────────────────────────┤
# │ 오늘의 시장 요약     │ 주요 지수 차트               │
# │ (AI 생성 텍스트)    │ KOSPI, KOSDAQ, 환율          │
# ├──────────────────────┴──────────────────────────────┤
# │ 거래량 상위 종목 / 급등락 종목                      │
# └─────────────────────────────────────────────────────┘
# ```

