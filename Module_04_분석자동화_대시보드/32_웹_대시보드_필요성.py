# ## 2. 대시보드 도구 비교
#
# ### Python 기반 대시보드 도구
#
# | 도구 | 장점 | 단점 | 적합한 용도 |
# |------|------|------|------------|
# | **Streamlit** | 쉬운 문법, 빠른 개발 | 고급 커스터마이징 제한 | 프로토타입, 개인 분석 |
# | **Dash (Plotly)** | 유연한 레이아웃, 콜백 | 학습 곡선 있음 | 프로덕션, 복잡한 앱 |
# | **Panel** | Jupyter 연동 | 커뮤니티 작음 | 데이터 사이언스 |
#
# ### 비코딩 도구
#
# | 도구 | 장점 | 단점 |
# |------|------|------|
# | **Tableau** | 강력한 시각화, 드래그앤드롭 | 유료, Python 연동 제한 |
# | **Power BI** | MS 생태계 통합 | Windows 중심 |
# | **Google Data Studio** | 무료, 웹 기반 | 기능 제한 |
#
# ### 이 과정에서 Streamlit을 선택한 이유
#
# 1. **Python 코드만으로 개발** - 새로운 언어 학습 불필요
# 2. **빠른 프로토타이핑** - 몇 줄의 코드로 대시보드 완성
# 3. **무료** - 오픈소스
# 4. **쉬운 배포** - Streamlit Cloud 무료 호스팅

